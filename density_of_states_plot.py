def plot_total_dos(energies, dos, cluster_name, fermi_energy=0.0, 
                   save_path=None, show=True):
    """
    Plot total density of states.
    """
    fig, ax = plt.subplots(figsize=(10, 6))
    
    ax.plot(energies, dos, 'b-', linewidth=2, label='Total DOS')
    
    # Vertical line at Fermi energy
    ax.axvline(x=0.0, color='r', linestyle='--', linewidth=1.5, label='Fermi level')
    
    # Fill between for occupied states
    mask = energies <= 0.0
    ax.fill_between(energies[mask], 0, dos[mask], alpha=0.3, color='blue')
    
    # Labels and formatting
    ax.set_xlabel('Energy (eV)', fontsize=12)
    ax.set_ylabel('DOS (states/eV)', fontsize=12)
    ax.set_title(f'Density of States - {cluster_name}', fontsize=14)
    ax.grid(True, alpha=0.3)
    ax.legend(loc='upper right')
    
    # Set x-axis limits
    ax.set_xlim(ENERGY_MIN, ENERGY_MAX)
    
    plt.tight_layout()
    
    if save_path:
        plt.savefig(save_path, dpi=300, bbox_inches='tight')
        print(f"Saved plot to {save_path}")
    
    if show:
        plt.show()
    else:
        plt.close()
    
    return fig, ax


