def calculate_dos(eigenvalues, occupations, fermi_energy, energy_min, energy_max, 
                  npoints=1000, sigma=0.1):
    """
    Calculate density of states from eigenvalues using Gaussian broadening.
    """
    energies = np.linspace(energy_min, energy_max, npoints)
    dos = np.zeros_like(energies)
    
    # Shift energies relative to Fermi level
    shifted_energies = eigenvalues - fermi_energy
    
    # Gaussian broadening
    for i, e_shift in enumerate(shifted_energies):
        if np.abs(e_shift) > 50:  # Skip far away states
            continue
        for j, e_grid in enumerate(energies):
            dos[j] += occupations[i] * np.exp(-((e_grid - e_shift)**2) / (2 * sigma**2))
    
    # Normalize
    if np.sum(dos) > 0:
        dos = dos / (sigma * np.sqrt(2 * np.pi)) * (energy_max - energy_min) / npoints
    
    return energies, dos


def calculate_pdos_from_eigenvalues(eigenvalues, occupations, fermi_energy, 
                                   energy_min, energy_max, orbital_weights, 
                                   npoints=1000, sigma=0.1):
    """
    Calculate projected DOS based on orbital weights.
    
    Parameters:
    -----------
    orbital_weights : dict
        Dictionary mapping orbital labels to weight arrays for each eigenvalue
    """
    energies = np.linspace(energy_min, energy_max, npoints)
    pdos_dict = {}
    
    for orbital_name, weights in orbital_weights.items():
        pdos = np.zeros_like(energies)
        shifted_energies = eigenvalues - fermi_energy
        
        for i, e_shift in enumerate(shifted_energies):
            if np.abs(e_shift) > 50:
                continue
            for j, e_grid in enumerate(energies):
                pdos[j] += weights[i] * occupations[i] * np.exp(-((e_grid - e_shift)**2) / (2 * sigma**2))
        
        # Normalize
        if np.sum(pdos) > 0:
            pdos = pdos / (sigma * np.sqrt(2 * np.pi)) * (energy_max - energy_min) / npoints
        
        pdos_dict[orbital_name] = pdos
    
    return energies, pdos_dict


