def extract_homo_lumo(energies, occupations, fermi_energy):
    """
    Extract HOMO and LUMO energies from eigenvalue data.
    """
    shifted_energies = energies - fermi_energy
    occupied_mask = occupations > 0.5
    
    if np.any(occupied_mask):
        homo_energy = np.max(shifted_energies[occupied_mask])
    else:
        homo_energy = shifted_energies[0]
    
    unoccupied_mask = occupations <= 0.5
    if np.any(unoccupied_mask):
        lumo_energy = np.min(shifted_energies[unoccupied_mask])
    else:
        lumo_energy = shifted_energies[-1]
    
    return homo_energy, lumo_energy, lumo_energy - homo_energy


def compute_band_gap(energies, dos, threshold=0.01):
    """
    Compute band gap from DOS data by finding energy gap around Fermi level.
    """
    # Find Fermi level index
    fermi_idx = np.argmin(np.abs(energies))
    
    # Look for gap (simplified method)
    # Find first occupied state above threshold
    # This is a simplified approach - actual gap determination
    # would require more sophisticated analysis
    
    gap = 0.0
    try:
        # Check for gap around Fermi level
        occupied = dos[:fermi_idx] > threshold
        unoccupied = dos[fermi_idx:] > threshold
        
        if np.any(occupied):
            homo_idx = np.max(np.where(occupied)[0])
        else:
            homo_idx = fermi_idx
            
        if np.any(unoccupied):
            lumo_idx = fermi_idx + np.min(np.where(unoccupied)[0])
        else:
            lumo_idx = fermi_idx
            
        gap = energies[lumo_idx] - energies[homo_idx]
    except:
        gap = 0.0
    
    return max(0, gap)


